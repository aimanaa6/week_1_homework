# first variable
first_name ='Aiman'
# second variable
last_name = 'Ahmed'
# generated both variables using print function (already produced a space)
print(first_name,last_name)
# use a full_name variable and add a space, the + signifies concatenation
full_name = first_name + ' ' + last_name
print(full_name)
# Lists contain elements and are mutable
name_list = ['Aiman', 'Ahmed']
print(name_list)
# Dictionary (associative) with keys - key:value
names = {'first':'Aiman', 'last': 'Ahmed'}
print(names)
print(names['first'])
print(names['last'])